#  Escape sequence charatcters :
#  These are Sequence of character after backslash holding special meaning
# 
#  \n : for new line

name = "He is a good boy \n he is intelligent"
print(name)
 
#  \t  : for tab space

name = "He is a good boy \t he is intelligent"
print(name)

# \ \  : for double quote in double quoted strings

name = "He is a good boy , his name is \"Chiku\""
print(name)