# Dictionary :->
#               it collect data in the key-value pairs.

# the main reason of using dictionary is to find the value by the 
# use of key, unlike in the 2D lists where we can access only by index

marks = {
    "Rohan" : 30,
    "Shashi" : 45,
    "Anshul" : 49
}

print(marks["Anshul"])

