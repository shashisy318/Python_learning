# Relational Operators

'''
==   : equals
>=   : greater than / equals
<=   : smaller than / equals
>   : greater than
<   : smaller than
!=  : not equal to

'''

# Logical Operators

'''
and  :- true only if both the conditions are true
or   :- true if any of the conditions are true 
not  :- invert true into false and false into true

'''
